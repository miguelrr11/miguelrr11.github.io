function encodeCanvas(canvas) {
    let dictionary = [];
    let indexMap = [];
    let map = new Map();

    for (let row of canvas) {
        let indexRow = [];
        for (let rgba of row) {
            let key = rgba.join(","); // Convert to a string for easy indexing
            if (!map.has(key)) {
                map.set(key, dictionary.length);
                dictionary.push(rgba);
            }
            indexRow.push(map.get(key));
        }
        indexMap.push(indexRow);
    }

    return { dictionary, indexMap };
}

function decodeCanvas(encoded) {
    const { dictionary, indexMap } = encoded;
    return indexMap.map(row => row.map(index => dictionary[index]));
}


